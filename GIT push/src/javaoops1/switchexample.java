package javaoops1;

public class switchexample {

	public static void main(String[] args) {

int month = 12 ;

switch(month){ 

case 1 : System.out.println("january-1");break;
case 2 : System.out.println("feb-2");break;
case 3 : System.out.println("march-3");break;
case 4 : System.out.println("april-4");break;

case 5 : System.out.println("may-5");break;

case 6 : System.out.println("june-6");break;

case 7 : System.out.println("july-7");break;

case 8 : System.out.println("aug-8");break;

case 9 : System.out.println("sept-9");break;

case 10 : System.out.println("oct-10");break;

case 11 : System.out.println("nov-11");break;

default:
 System.out.println("dec-12");
}

	
}
	
	
	
	
	
	
	
	
	
	
	
	
	}


